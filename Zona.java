public class Zona {
    String zona;
    double precioPorMinuto;

    public Zona(String zona, double precioPorMinuto) {
        this.zona = zona;
        this.precioPorMinuto = precioPorMinuto;
    }

    public String getZona() {
        return zona;
    }

    public double getPrecioPorMinuto() {
        return precioPorMinuto;
    }
}
